int main() {
    // Arity is the number of inputs an operator takes in

    // Unary. operator- is an unary operation
    int a{-5};

    // Binary. operator+ or operator<< are binary operations
    int b{1 + 2};

    // Ternary. Only the conditional operator 
    bool c = a == b ? true : false;

    // Nullary. Only the throw operator.
    // throw; Throws currently handled exception.
}
